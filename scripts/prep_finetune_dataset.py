"""scripts/prep_finetune_dataset.py

Day 16: convert raw synth traces → chatml-compatible messages, train/val split.

Input:  data/fine_tune/traces.jsonl    (generated by synth_tool_traces.py)
Output: data/fine_tune/train.jsonl
        data/fine_tune/val.jsonl

Gold traces (tests/data/gold_traces.jsonl) are the held-out test set —
never mixed into train or val. The day-18 eval script loads them separately.

Each output line format:
    {
        "id":             "synth_00001",
        "category":       "file_triage",
        "token_estimate": 812,
        "messages":       [{"role": "...", "content": "..."}, ...]
    }

The `messages` field is in chatml-compatible format so the day-17 notebook
can call tokenizer.apply_chat_template(messages, tokenize=False) directly.

OpenAI → chatml conversion:
  assistant + tool_calls  →  content = "<tool_call>\n{JSON}\n</tool_call>"
  tool result             →  role="tool", content = "<tool_response>\n...\n</tool_response>"

Usage:
    python scripts/prep_finetune_dataset.py
    python scripts/prep_finetune_dataset.py --val-frac 0.05
    python scripts/prep_finetune_dataset.py --preview          # show one converted trace, no write
    python scripts/prep_finetune_dataset.py --max-tokens 2048  # drop traces above token estimate
"""

from __future__ import annotations

import argparse
import json
import logging
import random
import sys
from pathlib import Path

logger = logging.getLogger(__name__)

REPO_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_IN = REPO_ROOT / "data" / "fine_tune" / "traces.jsonl"
DEFAULT_TRAIN_OUT = REPO_ROOT / "data" / "fine_tune" / "train.jsonl"
DEFAULT_VAL_OUT = REPO_ROOT / "data" / "fine_tune" / "val.jsonl"


# ---------------------------------------------------------------------------
# Format conversion: OpenAI tool-use → Qwen2.5 chatml-compatible messages
# ---------------------------------------------------------------------------

def _tool_call_content(tool_calls: list[dict]) -> str:
    """Render OpenAI tool_calls list as <tool_call> blocks."""
    parts: list[str] = []
    for tc in tool_calls:
        fn = tc.get("function", {})
        name = fn.get("name", "")
        args_raw = fn.get("arguments", "{}")
        try:
            args = json.loads(args_raw) if isinstance(args_raw, str) else args_raw
        except (json.JSONDecodeError, TypeError):
            args = args_raw
        parts.append(
            "<tool_call>\n"
            + json.dumps({"name": name, "arguments": args}, ensure_ascii=False)
            + "\n</tool_call>"
        )
    return "\n".join(parts)


def _tool_result_content(raw: str) -> str:
    """Wrap a tool result string in <tool_response> block."""
    try:
        parsed = json.loads(raw) if isinstance(raw, str) else raw
        body = json.dumps(parsed, ensure_ascii=False)
    except (json.JSONDecodeError, TypeError):
        body = str(raw)
    return f"<tool_response>\n{body}\n</tool_response>"


def convert_messages(openai_messages: list[dict]) -> list[dict]:
    """Convert OpenAI-format messages to chatml-compatible message list.

    Returns a list of {role, content} dicts suitable for
    tokenizer.apply_chat_template().
    """
    result: list[dict] = []
    for msg in openai_messages:
        role = msg.get("role", "")
        if role == "system":
            result.append({"role": "system", "content": msg.get("content") or ""})
        elif role == "user":
            result.append({"role": "user", "content": msg.get("content") or ""})
        elif role == "assistant":
            tool_calls = msg.get("tool_calls") or []
            if tool_calls:
                content = _tool_call_content(tool_calls)
            else:
                content = msg.get("content") or ""
            result.append({"role": "assistant", "content": content})
        elif role == "tool":
            raw = msg.get("content") or "{}"
            result.append({"role": "tool", "content": _tool_result_content(raw)})
    return result


# ---------------------------------------------------------------------------
# Token estimate (no tokenizer dependency — rough 4 chars/token heuristic)
# ---------------------------------------------------------------------------

def estimate_tokens(messages: list[dict]) -> int:
    total_chars = sum(len(m.get("content") or "") for m in messages)
    return total_chars // 4


# ---------------------------------------------------------------------------
# I/O helpers
# ---------------------------------------------------------------------------

def load_traces(path: Path) -> list[dict]:
    traces: list[dict] = []
    with path.open("r", encoding="utf-8") as fh:
        for lineno, line in enumerate(fh, 1):
            line = line.strip()
            if not line:
                continue
            try:
                traces.append(json.loads(line))
            except json.JSONDecodeError as exc:
                logger.warning("Skipping malformed line %d: %s", lineno, exc)
    return traces


def write_jsonl(records: list[dict], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as fh:
        for rec in records:
            fh.write(json.dumps(rec, ensure_ascii=False) + "\n")


# ---------------------------------------------------------------------------
# Stats
# ---------------------------------------------------------------------------

def percentile(sorted_vals: list[int], pct: float) -> int:
    if not sorted_vals:
        return 0
    idx = int(len(sorted_vals) * pct / 100)
    return sorted_vals[min(idx, len(sorted_vals) - 1)]


def print_stats(label: str, records: list[dict]) -> None:
    if not records:
        print(f"  {label}: 0 records")
        return
    tokens = sorted(r["token_estimate"] for r in records)
    cats: dict[str, int] = {}
    for r in records:
        c = r.get("category", "unknown")
        cats[c] = cats.get(c, 0) + 1
    print(f"\n  {label}: {len(records)} records")
    print(f"    tokens  p25={percentile(tokens, 25)}  p50={percentile(tokens, 50)}"
          f"  p75={percentile(tokens, 75)}  p95={percentile(tokens, 95)}  max={tokens[-1]}")
    for cat, n in sorted(cats.items(), key=lambda x: -x[1]):
        print(f"    {cat:<20} {n}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = argparse.ArgumentParser(
        description="Convert synth traces to chatml format and split train/val."
    )
    parser.add_argument("--in", dest="inp", type=Path, default=DEFAULT_IN,
                        help="Input traces.jsonl (default data/fine_tune/traces.jsonl)")
    parser.add_argument("--train-out", type=Path, default=DEFAULT_TRAIN_OUT)
    parser.add_argument("--val-out", type=Path, default=DEFAULT_VAL_OUT)
    parser.add_argument("--val-frac", type=float, default=0.04,
                        help="Fraction for validation split (default 0.04 = 4%%)")
    parser.add_argument("--max-tokens", type=int, default=0,
                        help="Drop traces whose token estimate exceeds this (0 = no cap)")
    parser.add_argument("--preview", action="store_true",
                        help="Print first converted trace and exit — no files written")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    # Windows consoles default to cp1252, which can't encode the '→' used in
    # the stats output. Force utf-8 so a clean run doesn't crash after writing.
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s  %(levelname)s  %(message)s",
        handlers=[logging.StreamHandler(sys.stdout)],
    )

    if not args.inp.exists():
        logger.error(
            "Input file not found: %s\n"
            "Run day-15 first:\n"
            "  python scripts/synth_tool_traces.py --count 5000",
            args.inp,
        )
        sys.exit(1)

    logger.info("Loading traces from %s ...", args.inp)
    raw_traces = load_traces(args.inp)
    logger.info("  Loaded %d lines", len(raw_traces))

    # Filter: only completed runs (not truncated by max_iterations)
    completed = [t for t in raw_traces if t.get("stop_reason") == "stop"]
    n_dropped_incomplete = len(raw_traces) - len(completed)
    if n_dropped_incomplete:
        logger.info("  Dropped %d incomplete traces (stop_reason != 'stop')", n_dropped_incomplete)

    # Convert + token estimate
    converted: list[dict] = []
    n_dropped_empty = 0
    for trace in completed:
        openai_msgs = trace.get("messages") or []
        chatml_msgs = convert_messages(openai_msgs)
        if not chatml_msgs:
            n_dropped_empty += 1
            continue
        tok = estimate_tokens(chatml_msgs)
        converted.append({
            "id": trace.get("id", ""),
            "category": trace.get("category", ""),
            "token_estimate": tok,
            "messages": chatml_msgs,
        })
    if n_dropped_empty:
        logger.info("  Dropped %d empty-message traces", n_dropped_empty)

    # Token cap
    if args.max_tokens > 0:
        before = len(converted)
        converted = [r for r in converted if r["token_estimate"] <= args.max_tokens]
        dropped = before - len(converted)
        if dropped:
            logger.info("  Dropped %d traces exceeding %d token estimate", dropped, args.max_tokens)

    if not converted:
        logger.error("No usable traces after filtering. Is day-15 complete?")
        sys.exit(1)

    # Preview mode
    if args.preview:
        sample = converted[0]
        print(f"\n--- Preview: {sample['id']} [{sample['category']}] "
              f"(~{sample['token_estimate']} tokens) ---\n")
        for msg in sample["messages"]:
            role = msg["role"].upper()
            content = msg["content"]
            print(f"[{role}]\n{content[:500]}{'...' if len(content) > 500 else ''}\n")
        return

    # Shuffle + split
    rng = random.Random(args.seed)
    rng.shuffle(converted)
    n_val = max(1, int(len(converted) * args.val_frac))
    val_set = converted[:n_val]
    train_set = converted[n_val:]

    # Write
    write_jsonl(train_set, args.train_out)
    write_jsonl(val_set, args.val_out)

    # Stats
    print("\n=== Dataset prep complete ===")
    print(f"  Raw traces loaded:   {len(raw_traces)}")
    print(f"  Incomplete dropped:  {n_dropped_incomplete}")
    print(f"  Empty dropped:       {n_dropped_empty}")
    print(f"  Usable:              {len(converted)}")
    print_stats(f"Train → {args.train_out.name}", train_set)
    print_stats(f"Val   → {args.val_out.name}", val_set)

    all_tokens = sorted(r["token_estimate"] for r in converted)
    n_over_2k = sum(1 for t in all_tokens if t > 2048)
    if n_over_2k:
        print(f"\n  WARNING: {n_over_2k} traces exceed 2048 token estimate."
              f" Consider --max-tokens 2048 to drop them before fine-tuning.")

    logger.info("Done. Train: %d  Val: %d", len(train_set), len(val_set))


if __name__ == "__main__":
    main()
